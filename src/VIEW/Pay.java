    private void PayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayButtonActionPerformed
        // TODO add your handling code here:
        Connection com;

        try {
            com = DriverManager.getConnection(Main.url, Main.user, Main.password);

                //String sqlStatement = "INSERT INTO member (name, lastname, email, birth, password) "
            //+ "VALUES(" + m_fName + "," + m_lName + ",20," + m_mail + "," + m_password1 + ")";
            for (int i = 0; i < Main.AllResa.size(); i++) {

                String sqlStatement = "INSERT INTO reservation (price, attractionName, mail, quantitÃ©, date) "
                        + "VALUES(?,?,?,?,?)";

                PreparedStatement pstmt = com.prepareStatement(sqlStatement);

                pstmt.setDouble(1, (double) Main.AllResa.get(i).getPrix());
                pstmt.setString(2, Main.AllResa.get(i).getNom());
                pstmt.setString(3, Main.AllResa.get(i).getLogin());
                
                String newDate = "";    //String we want
                String dayString = "";
                String MonthString = "";
                String YearString = "";
                int day, month, year;   //int buffer for the date

                day = Main.AllResa.get(i).getDate().getDate();
                
                if (day < 10) {
                    dayString = "0" + Integer.toString(day);

                } else {
                    dayString = Integer.toString(day);
                }              

                month = Main.AllResa.get(i).getDate().getMonth() + 1;

                if (month < 10) {
                    MonthString = "0" + Integer.toString(month);

                } else {
                    MonthString = Integer.toString(month);
                }

                year = Main.AllResa.get(i).getDate().getYear() + 1900;

                YearString = Integer.toString(year);

                newDate = dayString + "/" + MonthString + "/" + YearString;
                pstmt.setString(5, newDate);
                pstmt.setInt(4, Main.AllResa.get(i).getNombre());

                int rows = pstmt.executeUpdate();
            }

            com.close();

            Main.AllResa.clear();
            dispose();
            JOptionPane.showMessageDialog(null, "MERCI POUR VOTRE ACHAT");
            
        } catch (SQLException e) {
            System.out.println("exception : " + e.getMessage());
        }
    }//GEN-LAST:event_PayButtonActionPerformed

