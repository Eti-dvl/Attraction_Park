    private void ConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmButtonActionPerformed
        boolean success = true;

        String attractionName = AttractionNameTextField.getText();
        System.out.println(attractionName);
        int availability = Integer.parseInt(AvailabilityTextField1.getText());
        System.out.println(availability);
        
        String features = FeaturesTextField.getText();
        System.out.println(features);
        
        double price = Double.parseDouble(PriceTextField1.getText());
        System.out.println(price);

        if (attractionName.equals("")) {
            success = false;
            JOptionPane.showMessageDialog(null, "Missing Attraction name");
        } else if (availability == 0) {
            success = false;
            JOptionPane.showMessageDialog(null, "Wrong availability");
        } else if (features.equals("")) {
            success = false;
            JOptionPane.showMessageDialog(null, "Missing feature/description");
        } else if (price == 0) {
            success = false;
            JOptionPane.showMessageDialog(null, "Wrong price");
        }

        if (success == true) {
            //Constructor

            Connection com;

            try {
                com = DriverManager.getConnection(Main.url, Main.user, Main.password);

                //String sqlStatement = "INSERT INTO member (name, lastname, email, birth, password) "
                //+ "VALUES(" + m_fName + "," + m_lName + ",20," + m_mail + "," + m_password1 + ")";
                String sqlStatement = "INSERT INTO ride (name, availability, features, price, discount, image) "
                + "VALUES(?,?,?,?,?,?)";

                PreparedStatement pstmt = com.prepareStatement(sqlStatement);
                //java.util.Date ajdh = java.util.Calendar.getInstance().getTime();
                //System.out.println(ajdh);

                pstmt.setString(1, attractionName);
                pstmt.setInt(2, availability);
                pstmt.setString(3, features);
                pstmt.setDouble(4, price);
                pstmt.setInt(5, 0);
                pstmt.setString(6, "ride1");

                int rows = pstmt.executeUpdate();

                com.close();
                JOptionPane.showMessageDialog(null, "SUCCESS !\n" + attractionName + " HAS BEEN CREATED");
                EmployeeInterface empl = new EmployeeInterface();
                        empl.setVisible(true);
                dispose();
            } catch (SQLException e) {
                System.out.println("exception : " + e.getMessage());
                JOptionPane.showMessageDialog(null, "FAILURE IN RIDE CREATION");
            }

        }
    }//GEN-LAST:event_ConfirmButtonActionPerformed

