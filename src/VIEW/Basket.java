    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (Table.getSelectedRow() != -1) 
        {
            //remove selected row from the model and AllResa
            Main.AllResa.remove(Table.getSelectedRow());
            model.removeRow(Table.getSelectedRow());                                           
        }
        
        initPrice = 0.0;
        for(int i = 0 ; i< model.getRowCount() ; ++i)
        {
            initPrice+= (Double) model.getValueAt(i,3);            
        }
      
        discount=initPrice*Main.connected.getDiscount();
      
        totalPrice=initPrice-discount;
        
        initPriceLabel.setText(initPrice + " €");
        discountLabel.setText("- " + discount + " €");
        TotalValueLabel.setText(totalPrice + " €");
               
        
    }//GEN-LAST:event_jButton1ActionPerformed

