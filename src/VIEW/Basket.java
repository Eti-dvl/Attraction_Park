    private void initTable()
    {        
        String[] entetes = {"Ride", "Amount", "Date", "Price"};
        
        Object[][] donnees;
        donnees = new Object[Main.AllResa.size()][4];
        
        for(int i=0 ; i<Main.AllResa.size(); ++i)
        {
            //Simplify the way to print the date
            SimpleDateFormat s = new SimpleDateFormat("dd/MM/yyyy");
            Date date = Main.AllResa.get(i).getDate();
            System.out.println(s.format(date));
    
            donnees[i][0] = Main.AllResa.get(i).getNom();
            donnees[i][1] = Main.AllResa.get(i).getNombre();
            donnees[i][2] = s.format(date);
            donnees[i][3] = Main.AllResa.get(i).getPrix();
        }
        model = new DefaultTableModel(donnees,entetes){
            public boolean isCellEditable(int row, int column) {
            return false;}
        };
    }
    
